package com.example.a.checkattendance.Admin;

public class GroupInfo
{
    String name;
    int count;

    public GroupInfo(String name)
    {
        super();
        this.name = name;
        this.count = count;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getCount()
    {
        return count;
    }

    public void setCount(int count)
    {
        this.count = count;
    }

}
